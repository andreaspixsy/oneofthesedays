# It occurred to me today why <a href="http://www.jucy.co.nz/">Jucy Car Rentals</a> works. They have a brand that's cool, saucy, new, that people will want to show off. No one cares if you hired your car from Avis, or Budget, or another boring company. But if it's jucy, someone's going to ask about it. (If you haven't seen, they put suggestively posing women on the side of their campervans.)
# 
# This means that they can put advertising on the cars they rent out. It lets the renter tell a story, so why would they mind?
# 
# They can spend less on traditional (read: boring) advertising because they're not renting cars, they're renting mobile billboards, and so they can charge you less. Win-win.
# 
# Just like you can't out Amazon Amazon, you can't out Avis Avis. But if you're a backpacker, both the price point and the image is appealing, and so the choice is obvious.
# 
# They've found their [remarkable](http://sethgodin.typepad.com/seths_blog/2007/01/how_to_be_remar.html).
